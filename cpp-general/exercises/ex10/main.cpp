#include <iostream>
using namespace std;
int main() {
    short a;
    int b;
    long c;
    float d;
    double e;
    char f;
    bool g;
    cout << "short size: " << sizeof(a) << endl;
    cout << "int size: " << sizeof(b) << endl;
    cout << "long size: " << sizeof(c) << endl;
    cout << "float size: " << sizeof(d) << endl;
    cout << "double size: " << sizeof(e) << endl;
    cout << "char size: " << sizeof(f) << endl;
    cout << "bool size: " << sizeof(g) << endl;  
    return 0;
}
